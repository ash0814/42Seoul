#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	uc1;
	unsigned char	uc2;

	i = 0;
	while (s1[i] == s2[i] && s1[i] && s2[i] && i < n)
		i++;
	if (i == n)
		return (0);
	if (s1[i] == s2[i])
		return (0);
	uc1 = s1[i];
	uc2 = s2[i];
	return (uc1 - uc2);
}
