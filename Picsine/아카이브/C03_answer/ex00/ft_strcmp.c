#include <unistd.h>

int		ft_strcmp(char *s1, char *s2)
{
	int			i;
	unsigned	a[2];

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			break ;
		i++;
	}
	a[0] = s1[i];
	a[1] = s2[i];
	return (a[0] - a[1]);
}
