#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	char	*s;

	i = 0;
	while (src[i] != '\0')
		i++;
	s = (char *)malloc(i);
	i = -1;
	while (src[++i] != '\0')
	{
		s[i] = src[i];
	}
	return (s);
}
