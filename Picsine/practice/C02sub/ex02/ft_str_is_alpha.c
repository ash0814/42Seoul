int		ft_str_is_alpha(char *str)
{
	while (*str != '\0')
	{
		if (*str < 90 || *str > 97)
			return (0);
		else if (*str > 122 || *str < 65)
			return (0);
        else
			str++;
	}
	return (1);
}
